/**
 * Inserts the item into specified array at specified index
 *
 * @param {array} arr
 * @param {any} item
 * @param {number} index
 *
 * @example
 *    [ 1, 3, 4, 5 ], 2, 1  => [ 1, 2, 3, 4, 5 ]
 *    [ 1, 'b', 'c'], 'x',0  => [ 'x', 1, 'b', 'c' ]
 */
function insertItem(arr, item, index) {
  const result = [];
  for (let i = 0; i <= arr.length; i += 1) {
    const el = arr[i];
    if (index === i) {
      result.push(item);
    } else {
      result.push(el);
    }
  }
  return result;
}

